# 2. А және В екі бүтін сандар берілген. 
# А<B болса, өсу ретімен немесе басқаша жағдайда кему ретімен 
# А-дан В-ға дейінгі барлық сандарды басып шығарыңыз.
A=int(input())
B=int(input())
 
if A<B:                             #A<B өсу бойынша көрсетіледі
    for i in range(A, B+1):
        print(i)
else:                               #B<A кему бойынша
    for i in reversed(range(B, A+1)):
        print(i)